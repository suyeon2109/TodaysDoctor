RES_NUM NUMBER NOT NULL,
HP_CODE VARCHAR2(20) NOT NULL,
HP_NAME VARCHAR2(100) NOT NULL,
HP_INDEX VARCHAR2(20) NOT NULL,
HP_AREA VARCHAR2(20) NOT NULL,
HP_ADDRESS VARCHAR2(500) NOT NULL,
HP_PHONE VARCHAR2(20) NOT NULL,
