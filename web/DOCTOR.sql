RES_MIN NUMBER NOT NULL,
RES_DATE DATE NOT NULL,
HP_DOCTOR VARCHAR2(20) NOT NULL,
RES_CONTENT VARCHAR2(1000) NOT NULL,

CONSTRAINT RES_NUM_PK PRIMARY KEY (RES_NUM),
CONSTRAINT HP_CODE_FK2 FOREIGN KEY(HP_CODE) REFERENCES HPINFO(HP_CODE),
