HP_NAME VARCHAR2(500) NOT NULL,
HP_AVGPOINT NUMBER NOT NULL,
HP_POINT1 NUMBER NOT NULL,
HP_POINT2 NUMBER NOT NULL,
HP_POINT3 NUMBER NOT NULL,
HP_POINT4 NUMBER NOT NULL,
HP_POINT5 NUMBER NOT NULL,
REVIEW VARCHAR2(1000) NOT NULL,
G_POINT NUMBER NOT NULL,
N_POINT NUMBER NOT NULL,

CONSTRAINT RE_NUM_PK PRIMARY KEY (RE_NUM),
CONSTRAINT HP_CODE_FK FOREIGN KEY(HP_CODE) REFERENCES HPINFO(HP_CODE),
CONSTRAINT USER_FK FOREIGN KEY(USER_ID) REFERENCES USERINFO (USER_ID),
CONSTRAINT RES_NUM_FK FOREIGN KEY(RES_NUM) REFERENCES RESERVATION(RES_NUM)
);


